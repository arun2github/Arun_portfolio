// Dart model class for Education
class Education {
  final String title;
  final String collegeName;
  final String icon;
  final String iconBg;
  final String date;
  final String branch;
  final String percentage;

  Education({
    required this.title,
    required this.collegeName,
    required this.icon,
    required this.iconBg,
    required this.date,
    required this.branch,
    required this.percentage,
  });
}
